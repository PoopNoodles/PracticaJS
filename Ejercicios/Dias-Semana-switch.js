var dia = 3

switch(dia){
  case 1:
    console.log("lunes");
    break;
  case 2:
    console.log("martes");
    break;
  case 3:
    console.log("miercoles");
    break;
  case 4:
    console.log("jueves");
  case 5:
    console.log("viernes");
  case 6:
    console.log("sabado");
  case 7:
    console.log("domingo");
  default:
    console.log("WRONG");
}